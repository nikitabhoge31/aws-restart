# ----------- LIST -----------

myFruitList = ["apple", "banana", "cherry"]

print(myFruitList)
print(type(myFruitList))

# Access elements
print(myFruitList[0])
print(myFruitList[1])
print(myFruitList[2])

# Modify list
myFruitList[2] = "orange"
print(myFruitList)


# ----------- TUPLE -----------

